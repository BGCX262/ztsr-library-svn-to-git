    private void accept(){
        if (checkLogin()){
            this.setVisible(false);
            this.clearFields();
            menuPanel.activatePanel();
        }
    }
    private boolean checkLogin(){
       String login = this.loginTextField.getText();
        int loginInt =0;
        try{
            loginInt = Integer.parseInt(login);
            LoginData ldata = new LoginData(); // anonymous
            if (loginInt != 0){
                ldata = new LoginData(loginInt, new String(this.passField.getPassword()));
            }
            sData.setLogin(login);
            sData.logIn(ldata);
            
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "B³êdny login lub has³o (login musi byæ numerem karty)!","Uwaga",JOptionPane.WARNING_MESSAGE);
            return(false);
        }catch(RemoteException e){
            if (e.detail instanceof LibraryException) {
                JOptionPane.showMessageDialog(this, e.detail.getLocalizedMessage(),"Uwaga",JOptionPane.WARNING_MESSAGE);
            } else {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Problemy z po³¹czeniem","Uwaga",JOptionPane.WARNING_MESSAGE);
            }
            return(false);
        }
        return(true);
    }
    
