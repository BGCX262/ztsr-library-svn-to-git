    private void requestButtonAccept(){
        try{
            Collection<BookCopy> books = this.sData.getLibrary().getRequestedBooks(this.sData.getSession());
            requestedPanel.displayResults(books);
            this.setVisible(false);
            requestedPanel.setVisible(true);
        }catch(RemoteException e){
            JOptionPane.showMessageDialog(this, e.detail.getLocalizedMessage(),"Uwaga",JOptionPane.WARNING_MESSAGE);
        }
    }
    
